        เราจะไม่เปิดเผยข้อมูลส่วนบุคคลของท่านต่อบุคคลใดโดยปราศจากการอนุญาตจากท่าน อย่างไรก็ดี ท่านรับทราบและยินยอมว่าเราอาจใช้บริการจากผู้ให้บริการภายในหรือภายนอก เพื่อประโยชน์ในการให้บริการเว็บไซต์นี้ หรือเพื่อปรับปรุงและพัฒนารูปแบบการให้บริการและการเข้าถึงเนื้อหาต่าง ๆ ในเว็บไซต์ การรักษาความมั่นคงปลอดภัยของเว็บไซต์รวมถึงการดำเนินงานด้านอื่น ๆ ในนามของเรา เพื่อที่จะให้บริการตามที่ท่านได้ร้องขอ รวมถึงเพื่อประโยชน์ในการเพิ่มประสิทธิภาพการให้บริการในด้านต่าง ๆ แก่ท่านมากยิ่งขึ้น โดยผู้ให้บริการเหล่านี้อาจจะเข้าถึงข้อมูลส่วนบุคคลของท่าน เราจะดำเนินการให้บุคคลเหล่านั้นเก็บรักษาข้อมูลส่วนบุคคลของท่านไว้เป็นความลับ และไม่นำไปใช้เพื่อวัตถุประสงค์อื่นนอกเหนือจากขอบเขตที่เราได้กำหนดไว้
    
        เราจะไม่ดำเนินการขายข้อมูลส่วนบุคคลของท่านให้แก่บุคคลที่ไม่เกี่ยวข้องใด ๆ เพื่อวัตถุประสงค์ทางการตลาด
    
        อย่างไรก็ตาม เราอาจเปิดเผยข้อมูลส่วนบุคคลของท่านภายใต้หลักเกณฑ์ที่กฎหมายกำหนด เช่น การเปิดเผยข้อมูลต่อหน่วยงานราชการ หน่วยงานภาครัฐ หน่วยงานที่มีหน้าที่กำกับดูแลการให้บริการ หรือหน่วยงานกำกับดูแลท่าน รวมถึงในกรณีที่มีการร้องขอให้เปิดเผยข้อมูลโดยอาศัยอำนาจตามกฎหมาย อาทิ การร้องขอข้อมูลเพื่อการฟ้องร้องหรือดำเนินคดีตามกฎหมาย หรือเป็นการร้องขอจากหน่วยงานเอกชน หรือบุคคลภายนอกอื่น ๆ ที่มีความเกี่ยวข้องกับกระบวนการทางกฎหมาย รวมถึงในกรณีที่มีความจำเป็นตามสมควรในการบังคับใช้ข้อตกลงและเงื่อนไขการใช้ หรือเพื่อปกป้องประโยชน์ สิทธิ และทรัพย์สินของเราหรือการขายหรือการโอนทรัพย์สิน หรือขอบเขตที่คล้ายคลึงกันในการเจรจาทางธุรกิจ ตลอดจนการเปิดเผยข้อมูลในกรณีที่มีการปรับโครงสร้างองค์กร การควบรวมเรา เราอาจถ่ายโอนข้อมูลส่วนบุคคลของท่านไม่ว่าทั้งหมดหรือบางส่วนที่เราเก็บรวบรวมไว้ไปยังเราที่เกี่ยวข้อง ทั้งนี้ เราจะดำเนินการตามสมควรให้เราผู้รับโอนปฏิบัติตามเงื่อนไขในนโยบายความเป็นส่วนตัวฉบับนี้
    <p>
    <p>
        <strong>มาตรการในการรักษาความปลอดภัยของข้อมูลส่วนบุคคล</strong><br>
        เรารับรองว่าจะจัดเก็บ รักษา และสงวนไว้ซึ่งข้อมูลส่วนบุคคลของท่านอย่างปลอดภัยและเป็นความลับ โดยจะบังคับใช้มาตรการต่าง ๆ อย่างเหมาะสมและตามสมควรเพื่อดูแลความปลอดภัย ปกป้องการเข้าถึง การนำไปใช้งานในทางที่ผิด การป้องกันไม่ให้มีการเข้าสู่ระบบคอมพิวเตอร์ที่ไม่ได้รับอนุญาต หรือการเปิดเผยข้อมูลส่วนบุคคลของท่าน รวมทั้งจำกัดการเข้าถึงข้อมูลส่วนบุคคลของท่านจากพนักงาน ลูกจ้าง และตัวแทนของเรา ซึ่งได้รับข้อมูลส่วนบุคคลของท่านดำเนินตามมาตรการเดียวกับเรา
        
        เราจะเก็บรักษาและดำเนินการให้ผู้ให้บริการอื่น ๆ ของเราเก็บรักษาข้อมูลส่วนบุคคลของท่านในระยะเวลาเท่าที่จำเป็นเพื่อจุดประสงค์ตามนโยบายความเป็นส่วนตัวนี้ รวมถึงข้อกำหนดและกฎหมายที่เกี่ยวข้อง
        
        เราไม่สามารถรับประกันถึงความปลอดภัยทั้งหมดในข้อมูลส่วนบุคคลของท่านที่ถูกถ่ายโอนมายังเว็บไซต์ของเรา ผ่านระบบอินเตอร์เน็ต แต่เราจะดำเนินการอย่างเข้มงวดเพื่อรักษาความปลอดภัย และป้องกันการเข้าถึงข้อมูลส่วนบุคคลของท่านโดยบุคคลอื่นที่ไม่ได้รับอนุญาต
    </p>
    <p>
        <strong>สิทธิของท่านในฐานะเจ้าของข้อมูลส่วนบุคคล</strong><br>
        เรารับรองว่าในระหว่างการเก็บรักษาข้อมูลส่วนบุคคลของท่าน ท่านในฐานะเจ้าของข้อมูลมีสิทธิดำเนินการดังต่อไปนี้
    </p>
    <ul>
        <li>เพิกถอนความยินยอมใด ๆ ที่ท่านได้ให้ไว้กับเรา ไม่ว่าทั้งหมดหรือแต่บางส่วน</li>
        <li>ขอเข้าถึงข้อมูลและขอรับสำเนาข้อมูลส่วนบุคคลที่เกี่ยวกับท่านซึ่งอยู่ในความรับผิดชอบของเรา หรือขอให้เปิดเผยถึงการได้มาซึ่งข้อมูลส่วนบุคคลที่ท่านไม่ได้ให้ความยินยอม</li>
        <li>ขอรับข้อมูลส่วนบุคคลที่เกี่ยวกับท่านจากเรา และขอให้เราส่งหรือโอนข้อมูลส่วนบุคคลของท่านไปยังบุคคลอื่น รวมถึงขอรับข้อมูลส่วนบุคคลที่เราได้ส่งหรือโอนไปนั้นโดยตรง</li>
