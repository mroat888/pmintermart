        <li>กรณีที่เราไม่มีความจำเป็นในการเก็บรักษาข้อมูลส่วนบุคคลตามวัตถุประสงค์ในการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลอีกต่อไป</li>
        <li>กรณีท่านได้เพิกถอนความยินยอมในการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูล และเราไม่มีอำนาจตามกฎหมายในการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลได้ต่อไป</li>
        <li>กรณีท่านคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลที่เกี่ยวกับท่าน</li>
        <li>ขอให้เราระงับการใช้ข้อมูลส่วนบุคคล ในกรณีที่ไม่มีความจำเป็นในการเก็บรักษาข้อมูลส่วนบุคคลตามวัตถุประสงค์ในการเก็บรวบรวมอีกต่อไป</li>
        <li>ขอให้เราดำเนินการทำให้ข้อมูลส่วนบุคคลที่เกี่ยวกับท่านถูกต้อง เป็นปัจจุบัน สมบูรณ์ และไม่ก่อให้เกิดความเข้าใจผิด</li>
